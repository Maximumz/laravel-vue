     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $product = new Product;
        // Validate Request
        if ($product->validateRequest($request)) {
          $productData = $product->processRequest($request);
          if ($product->saveProduct($productData)) {
            return back()->with([
              'message' => 'Your product has been saved!',
            ]);
          }
        }

        return back()->with([
          'message' => 'Please make sure all the fields are filled!',
        ]);
    }

